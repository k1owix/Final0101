CREATE TABLE Users (
    UserId INT IDENTITY PRIMARY KEY,
    FullName NVARCHAR(100) NOT NULL,
    [Login] NVARCHAR(100) UNIQUE NOT NULL,
    [Password] NVARCHAR(50) NOT NULL,
    [Role] NVARCHAR(30) NOT NULL
);

CREATE TABLE Products (
    ArticleNumber NVARCHAR(20) PRIMARY KEY,
    [Name] NVARCHAR(255) NOT NULL,
    Unit NVARCHAR(20) NOT NULL,
    Price DECIMAL(10,2) NOT NULL,
    Author NVARCHAR(255) NULL,
    Manufacturer NVARCHAR(255) NOT NULL,
    Category NVARCHAR(100) NOT NULL,
    Discount INT NOT NULL DEFAULT 0,
    StockQuantity INT NOT NULL DEFAULT 0,
    [Description] NVARCHAR(MAX) NULL,
    Photo NVARCHAR(500) NULL
);

CREATE TABLE Orders (
    OrderId INT IDENTITY PRIMARY KEY,
    OrderDate DATETIME NOT NULL DEFAULT GETDATE(),
    DeliveryDate DATETIME NULL,
    CustomerId INT NULL,
    CustomerName NVARCHAR(100) NULL,
    PickupCode CHAR(3) NOT NULL,
    [Status] NVARCHAR(20) NOT NULL DEFAULT 'Новый',
    CONSTRAINT FK_Orders_Users FOREIGN KEY (CustomerId) REFERENCES Users(UserId) ON DELETE SET NULL
);

CREATE TABLE OrderProducts (
    Id INT IDENTITY PRIMARY KEY,
    OrderId INT NOT NULL,
    ProductId NVARCHAR(20) NOT NULL,
    Quantity INT NOT NULL DEFAULT 1,
    CONSTRAINT FK_OrderProducts_Orders FOREIGN KEY (OrderId) REFERENCES Orders(OrderId) ON DELETE CASCADE,
    CONSTRAINT FK_OrderProducts_Products FOREIGN KEY (ProductId) REFERENCES Products(ArticleNumber)
);

